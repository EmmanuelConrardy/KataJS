export function add(x: number, y: number) :number {
  return x + y;
}

export function minus(x: number, y: number):number {
  return x - y;
}
export function divide(x: number, y: number):number {
  return x / y;
}
export function multiply(x: number, y: number):number {
  return x * y;
}

